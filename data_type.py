# DataType Output: str
x = "Hello World"
print((x),"\n",type(x))

# DataType Output: int
y = 50
print((y),type(y))

# DataType Output: float
z = 60.5
print((z),type(z))

# DataType Output: complex
a = 3j
print((a),type(a))

# DataType Output: list
b = ["geeks", "for", "geeks"]
print((b),type(b))

# DataType Output: tuple
c = ("geeks", "for", "geeks")
print((c),type(c))

# DataType Output: range
d = range(10)
print((d),type(d))

# DataType Output: dict
e = {"name": "Suraj", "age": 24}
print((e),type(e))

# DataType Output: set
f = {"geeks", "for", "geeks"}
print((f),type(f))
# DataType Output: frozenset
g = frozenset({"geeks", "for", "geeks"})
print((g),type(g))
# DataType Output: bool
h = True
print((h),type(h))

# DataType Output: bytes
i = b"Geeks"
print((i),type(i))

# DataType Output: bytearray
j = bytearray(4)
print((j),type(j))
# DataType Output: memoryview
k = memoryview(bytes(6))
print((k),type(k))

# DataType Output: NoneType
l = None
print((l),type(l))
